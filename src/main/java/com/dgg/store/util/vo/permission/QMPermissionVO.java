package com.dgg.store.util.vo.permission;

public class QMPermissionVO
{
    private String permissionId;

    private String permissionName;

    private String permissionDescribe;

    private String permissionPid;

    private String permissionRe;

    private String permissionType;

    public String getPermissionId()
    {
        return permissionId;
    }

    public void setPermissionId(String permissionId)
    {
        this.permissionId = permissionId;
    }

    public String getPermissionName()
    {
        return permissionName;
    }

    public void setPermissionName(String permissionName)
    {
        this.permissionName = permissionName;
    }

    public String getPermissionDescribe()
    {
        return permissionDescribe;
    }

    public void setPermissionDescribe(String permissionDescribe)
    {
        this.permissionDescribe = permissionDescribe;
    }

    public String getPermissionPid()
    {
        return permissionPid;
    }

    public void setPermissionPid(String permissionPid)
    {
        this.permissionPid = permissionPid;
    }

    public String getPermissionRe()
    {
        return permissionRe;
    }

    public void setPermissionRe(String permissionRe)
    {
        this.permissionRe = permissionRe;
    }

    public String getPermissionType()
    {
        return permissionType;
    }

    public void setPermissionType(String permissionType)
    {
        this.permissionType = permissionType;
    }
}
