package com.dgg.store.util.pojo;

public class ShoppingAddress {

    private String userId;

    private String userAddressId;

    private String userAddressText;

    private String userAddressArea;

    private String userAddressName;

    private String userAddressPhone;

    public String getUserId()
    {
        return userId;
    }

    public void setUserId(String userId)
    {
        this.userId = userId;
    }

    public String getUserAddressId()
    {
        return userAddressId;
    }

    public void setUserAddressId(String userAddressId)
    {
        this.userAddressId = userAddressId;
    }

    public String getUserAddressText()
    {
        return userAddressText;
    }

    public void setUserAddressText(String userAddressText)
    {
        this.userAddressText = userAddressText;
    }

    public String getUserAddressArea()
    {
        return userAddressArea;
    }

    public void setUserAddressArea(String userAddressArea)
    {
        this.userAddressArea = userAddressArea;
    }

    public String getUserAddressName()
    {
        return userAddressName;
    }

    public void setUserAddressName(String userAddressName)
    {
        this.userAddressName = userAddressName;
    }

    public String getUserAddressPhone()
    {
        return userAddressPhone;
    }

    public void setUserAddressPhone(String userAddressPhone)
    {
        this.userAddressPhone = userAddressPhone;
    }
}